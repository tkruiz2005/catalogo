export const IconForAssetType = (
    { type, ...props }: { type?: string } & React.SVGProps<SVGSVGElement>
): JSX.Element => {
    switch (type?.toLowerCase()) {
        case 'servidor':
            return <ServerIcon {...props} />;
        case 'base de datos':
            return <DatabaseIcon {...props} />;
        default:
            return <GlobeIcon {...props} />;
    }
};